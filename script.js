function setAlarmPreset(minutes) {
  if (state.alarmModalStation && state.alarmModalType) {
    const id = state.alarmModalStation.id;
    const list = state.alarmModalType === 'ps5' ? state.ps5Stations : state.xboxStations;
    const station = list.find(s => s.id === id);
    if (station) {
      station.alarmMinutes = minutes;
      station.alarmTriggered = false;
      saveState();
      renderAll();
    }
  }
  closeAlarmModal();
}

function setAlarmCustom() {
  const val = parseInt($('alarm-custom-input').value);
  if (val && val > 0) {
    setAlarmPreset(val);
  }
}

function turnOffAlarm() {
  if (state.alarmModalStation && state.alarmModalType) {
    const id = state.alarmModalStation.id;
    const list = state.alarmModalType === 'ps5' ? state.ps5Stations : state.xboxStations;
    const station = list.find(s => s.id === id);
    if (station) {
      station.alarmMinutes = null;
      station.alarmTriggered = false;
      saveState();
      renderAll();
    }
  }
  closeAlarmModal();
}

