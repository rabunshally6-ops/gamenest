function loadAllData() {
  try {
    const s = localStorage.getItem('gc-settings');
    const p = localStorage.getItem('gc-ps5-stations');
    const x = localStorage.getItem('gc-xbox-stations');
    const b = localStorage.getItem('gc-billiard-tables');
    const g = localStorage.getItem('gc-games');
    const m = localStorage.getItem('gc-menu-items');
    const r = localStorage.getItem('gc-reservations');
    const n = localStorage.getItem('gc-notifications');
    const rev = localStorage.getItem('gc-revenue');
    const ban = localStorage.getItem('gc-banned-users');

    if (s) state.settings = JSON.parse(s);
    if (p) state.ps5Stations = JSON.parse(p);
    if (x) state.xboxStations = JSON.parse(x);
    if (b) state.billiardTables = JSON.parse(b);
    if (g) state.games = JSON.parse(g);
    if (m) state.menuItems = JSON.parse(m);
    if (r) state.reservations = JSON.parse(r);
    if (n) {
      const parsed = JSON.parse(n);
      state.notifications = parsed;
      state.unreadCount = parsed.filter(nn => !nn.isRead).length;
    }
    if (rev) state.revenue = JSON.parse(rev);
    else state.revenue = [];
    if (ban) state.bannedUsers = JSON.parse(ban);
    else state.bannedUsers = [];
  } catch (e) { console.error('Load error:', e); }
}

