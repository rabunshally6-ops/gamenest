function updatePS5Station(id, updates) {
  const station = state.ps5Stations.find(s => s.id === id);
  if (!station) return;

  if (updates._pause) {
    station.isPaused = true;
    station.pausedAt = new Date().toISOString();
  } else if (updates._resume) {
    if (station.pausedAt) {
      const pausedDuration = Date.now() - new Date(station.pausedAt).getTime();
      const newStart = new Date(new Date(station.startTime).getTime() + pausedDuration);
      station.startTime = newStart.toISOString();
    }
    station.isPaused = false;
    station.pausedAt = null;
  }

  if (updates.isRunning === false && updates._wasRunning) {
    // Stopping - record revenue
    const rate = getHourlyRatePS5(station.currentGame?.name || null);
    const runningCost = station.startTime ? calcRunningCost(station.startTime, rate, false, null) : 0;
    const totalCost = runningCost + (station.orderCost || 0);

    if (totalCost > 0) {
      state.revenue = state.revenue || [];
      state.revenue.push({
        id: generateId(),
        type: 'ps5',
        stationName: station.name,
        gameCost: runningCost,
        snackCost: station.orderCost || 0,
        totalCost: totalCost,
        gameName: station.currentGame?.name || '-',
        duration: station.startTime ? calcElapsed(station.startTime, false, null) : 0,
        timestamp: new Date().toISOString(),
        date: new Date().toLocaleDateString(),
      });
    }

    station.isRunning = false;
    station.isPaused = false;
    station.startTime = null;
    station.pausedAt = null;
    station.orderItems = '[]';
    station.orderCost = 0;
    station.alarmMinutes = null;
    station.alarmTriggered = false;
  } else if (updates.isRunning === true) {
    // Starting
    const gameId = updates.currentGameId !== undefined ? updates.currentGameId : station.currentGameId;
    const game = state.games.find(g => g.id === gameId);
    station.currentGame = game || null;
    if (updates.currentGameId !== undefined) station.currentGameId = updates.currentGameId;
    station.isRunning = true;
    station.isPaused = false;
    station.startTime = updates.startTime || new Date().toISOString();
    station.pausedAt = null;
    station.orderItems = '[]';
    station.orderCost = 0;
  }

  // Copy other updates
  if (updates.joystickCount !== undefined) station.joystickCount = updates.joystickCount;
  if (updates.currentGameId !== undefined && !updates.isRunning) {
    station.currentGameId = updates.currentGameId === null ? null : updates.currentGameId;
    const game = state.games.find(g => g.id === updates.currentGameId);
    station.currentGame = game || null;
  }
  if (updates.isReserved !== undefined) { station.isReserved = updates.isReserved; station.reservedBy = updates.reservedBy; }
  if (updates.alarmMinutes !== undefined) station.alarmMinutes = updates.alarmMinutes;
  if (updates.alarmTriggered !== undefined) station.alarmTriggered = updates.alarmTriggered;

  saveState();
  renderAll();
}

function toggleOrderPS5(id) {
  const station = state.ps5Stations.find(s => s.id === id);
  if (!station) return;
  const items = state.menuItems;
  if (items.length === 0) { showToast(t().addMenuItem, 'error'); return; }

  const tt = t();
  let orderItems = station.orderItems ? JSON.parse(station.orderItems) : [];

  const itemNames = items.map((item, i) => `${i+1}. ${item.name} - ${formatMoney(item.price)}`).join('\n');
  const choice = prompt(`${tt.addToOrder}:\n${itemNames}\n\n${state.language === 'en' ? 'Enter item number:' : 'ژمارەی بابەت بنووسە:'}`);
  if (!choice) return;

  const idx = parseInt(choice) - 1;
  if (idx >= 0 && idx < items.length) {
    const existing = orderItems.find(oi => oi.id === items[idx].id);
    if (existing) { existing.qty++; }
    else { orderItems.push({ id: items[idx].id, name: items[idx].name, price: items[idx].price, qty: 1 }); }
    station.orderItems = JSON.stringify(orderItems);
    station.orderCost = orderItems.reduce((sum, oi) => sum + oi.price * oi.qty, 0);
    saveState();
    renderAll();
  }
}

