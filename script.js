// ===================== HELPERS =====================
function formatTime(ms) {
  const hrs = Math.floor(ms / 3600000);
  const mins = Math.floor((ms % 3600000) / 60000);
  const secs = Math.floor((ms % 60000) / 1000);
  return `${String(hrs).padStart(2,'0')}:${String(mins).padStart(2,'0')}:${String(secs).padStart(2,'0')}`;
}

function formatMoney(amount) {
  return amount.toLocaleString() + ' IQD';
}

function getHourlyRatePS5(gameName) {
  if (!state.settings) return 3000;
  if (gameName === 'Black Ops 3') return state.settings.ps5BlackOps3Rate;
  return state.settings.ps5HourlyRate;
}

function calcRunningCost(startTime, rate, isPaused, pausedAt) {
  const endTime = isPaused && pausedAt ? new Date(pausedAt).getTime() : Date.now();
  const elapsed = endTime - new Date(startTime).getTime();
  return Math.round((elapsed / 3600000) * rate);
}

function calcElapsed(startTime, isPaused, pausedAt) {
  const endTime = isPaused && pausedAt ? new Date(pausedAt).getTime() : Date.now();
  return endTime - new Date(startTime).getTime();
}

function getPerSecondRate(hourlyRate) {
  return (hourlyRate / 3600).toFixed(2);
}

function showToast(message, type = 'info') {
  const container = document.getElementById('toast-container');
  const toast = document.createElement('div');
  toast.className = `toast toast-${type}`;
  toast.textContent = message;
  container.appendChild(toast);
  setTimeout(() => { toast.remove(); }, 3000);
}

