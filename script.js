function startTimers() {
  setInterval(() => {
    // Update live timers
    document.querySelectorAll('.live-timer, .station-timer-time').forEach(el => {
      const start = el.dataset.start;
      const isPaused = el.dataset.paused === 'true';
      const pausedAt = el.dataset.pausedat;
      if (!start) return;
      const elapsed = calcElapsed(start, isPaused, pausedAt);
      el.textContent = formatTime(elapsed);
    });

    // Check alarms
    [...state.ps5Stations, ...state.xboxStations].forEach(station => {
      if (station.isRunning && station.alarmMinutes && !station.alarmTriggered && !station.isPaused && station.startTime) {
        const elapsed = Date.now() - new Date(station.startTime).getTime();
        if (elapsed >= station.alarmMinutes * 60000) {
          station.alarmTriggered = true;
          // Add notification
          state.notifications.unshift({
            id: generateId(), type: 'alarm', message: `${station.name} - Alarm!`,
            isRead: false, createdAt: new Date().toISOString(),
          });
          state.unreadCount++;
          saveState();
          try { new Audio('data:audio/wav;base64,UklGRl9vT19teleXBl').play(); } catch(e) {}
          renderAll();
        }
      }
    });

    // Update running costs
    if (state.isAdmin) {
      document.querySelectorAll('.station-timer-time-blue[data-start]').forEach(el => {
        const card = el.closest('.station-card');
        if (!card) return;
        const costEl = card.querySelector('.station-timer-cost-blue');
        if (costEl) {
          const startTime = el.dataset.start;
          const isPaused = el.dataset.paused === 'true';
          const pausedAt = el.dataset.pausedat;
          const station = state.ps5Stations.find(s => s.startTime === startTime) || state.ps5Stations[0];
          const rate = station ? getHourlyRatePS5(station.currentGame?.name || null) : 3000;
          const runningCost = calcRunningCost(startTime, rate, isPaused, pausedAt || null);
          costEl.textContent = formatMoney(runningCost);
        }
      });
      document.querySelectorAll('.station-timer-time-green[data-start]').forEach(el => {
        const card = el.closest('.station-card');
        if (!card) return;
        const costEl = card.querySelector('.station-timer-cost-green');
        if (costEl) {
          const startTime = el.dataset.start;
          const isPaused = el.dataset.paused === 'true';
          const pausedAt = el.dataset.pausedat;
          const rate = state.settings?.xboxHourlyRate || 3000;
          const runningCost = calcRunningCost(startTime, rate, isPaused, pausedAt || null);
          costEl.textContent = formatMoney(runningCost);
        }
      });
    }
  }, 1000);
}

