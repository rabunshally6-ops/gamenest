function recordBilliard(id) {
  const table = state.billiardTables.find(t => t.id === id);
  if (!table) return;
  const sRate = state.settings?.billiardSingleRate || 750;
  const dRate = state.settings?.billiardDoubleRate || 1500;
  const gameCost = (table.singleGamesPlayed || 0) * sRate + (table.doubleGamesPlayed || 0) * dRate;
  const totalCost = gameCost + (table.orderCost || 0);

  if (totalCost > 0) {
    state.revenue = state.revenue || [];
    state.revenue.push({
      id: generateId(), type: 'billiard', stationName: table.name,
      gameCost: gameCost, snackCost: table.orderCost || 0,
      totalCost: totalCost, singleGames: table.singleGamesPlayed,
      doubleGames: table.doubleGamesPlayed,
      timestamp: new Date().toISOString(), date: new Date().toLocaleDateString(),
    });
  }

  table.singleGamesPlayed = 0; table.doubleGamesPlayed = 0;
  table.gamesPlayed = 0; table.orderItems = '[]'; table.orderCost = 0;
  saveState(); renderAll();
}

